             $(this).html(response);
            }
        });
    }
    
    function prevPage(batas, kategori, classResponseDiv){
        nextIndex -= batas;
         var pagination = "<?php echo site_url('news/pagination/')?>" + nextIndex + "/" + batas+ "/" + kategori;
         console.log(pagination);
         if(nextIndex == 0){
                    $(".next-" + classResponseDiv).css("display", "inline");
